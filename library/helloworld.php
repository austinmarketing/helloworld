// loading modernizr and jquery, and reply script
function helloworld_scripts_and_styles() {

  global $wp_styles; // call global $wp_styles variable to add conditional wrapper around ie stylesheet the WordPress way

  if (!is_admin()) {

		// modernizr (without media query polyfill)
		wp_register_script( 'helloworld-modernizr', get_stylesheet_directory_uri() . '/library/js/libs/modernizr.custom.min.js', array(), '2.5.3', false );

		// register main stylesheet
		wp_register_style( 'helloworld-stylesheet', get_stylesheet_directory_uri() . '/library/css/theme.css', array(), '', 'all' );
		// register main stylesheet - REPLACE WITH CODE BELOW WHEN IN PRODUCTION
		//wp_register_style( 'helloworld-stylesheet', get_stylesheet_directory_uri() . '/library/css/theme.css?v=1.0', array(), '', 'all' );

    // comment reply script for threaded comments
    if ( is_singular() AND comments_open() AND (get_option('thread_comments') == 1)) {
		  wp_enqueue_script( 'comment-reply' );
    }

		//adding scripts file in the footer
		wp_register_script( 'helloworld-js', get_stylesheet_directory_uri() . '/library/js/scripts.js', array( 'jquery' ), '', true );

		// enqueue styles and scripts
		wp_enqueue_script( 'helloworld-modernizr' );
		wp_enqueue_style( 'helloworld-stylesheet' );
		
		/*
		I recommend using a plugin to call jQuery
		using the google cdn. That way it stays cached
		and your site will load faster.
		*/
		wp_enqueue_script( 'jquery' );
		wp_enqueue_script( 'helloworld-js' );

	}
}

